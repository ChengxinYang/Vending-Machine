package VendingMachine;

public class Coin {
	public float weight;
	public float diameter;
	public void setWeight(float weight){
		this.weight = weight;
	};
	public float getWeight(){
		return weight;
	}
	public void setDiameter(float diameter){
		this.diameter = diameter;
	}
	public float getDiameter(){
		return diameter;
	}
}
